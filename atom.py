
import point as pt
import numpy as np

class atom(pt.point):
    def __init__(self, x, y, z, charge, sigma, epsilon, mass):
        super().__init__(x,y,z)
        self.charge = charge
        self.sigma = sigma
        self.epsilon = epsilon
        self.mass = mass

    def magnitude(self,atom):
        return self.distance(atom)

    def jointSigma(self,atom):
        return (self.sigma+atom.sigma)/2

    def jointEpsilon(self,atom):
        return np.sqrt(self.epsilon * atom.epsilon)


